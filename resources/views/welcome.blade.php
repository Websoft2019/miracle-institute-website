  <h2>Test Preparation</h2>
  <p><img width="1280" height="720" src="data:image/gif;base64,R0lGODdhAQABAPAAAP///wAAACwAAAAAAQABAEACAkQBADs=" alt="wAAACwAAAAAAQABAEACAkQBADs="><img width="1280" height="720" src="wp-content/uploads/2019/10/IELTS.jpg" alt="IELTS"></p>
  <h4>IELTS</h4>
  <p>The International English Language Testing System (IELTS) is a test of English language proficiency that is required to be taken by non-native students for study in country where the medium of instruction is English.</p>
  <p><a href="ielts/index.html">Read more</a></p>
  <p><img width="600" height="401" src="data:image/gif;base64,R0lGODdhAQABAPAAAP///wAAACwAAAAAAQABAEACAkQBADs=" alt="wAAACwAAAAAAQABAEACAkQBADs="><img width="600" height="401" src="wp-content/uploads/2019/10/toefl.png" alt="toefl"></p>
  <h4>TOEFL</h4>
  <p>TOEFL is a highly reliable and valid standardized test that evaluates the English Proficiency of people whose native language is not English. The TOEFL measures the ability to understand North American English.</p>
  <p><a href="toefl/index.html">Read more</a></p>
  <p><img width="2000" height="1386" src="data:image/gif;base64,R0lGODdhAQABAPAAAP///wAAACwAAAAAAQABAEACAkQBADs=" alt="wAAACwAAAAAAQABAEACAkQBADs="><img width="2000" height="1386" src="wp-content/uploads/2019/10/sat_logo.png" alt="sat_logo"></p>
  <h4>SAT</h4>
  <p>The SAT tests students' reasoning based on knowledge and skills developed through their course work. It measures their ability to analyze and solve problems by applying what they have learned in school.</p>
  <p><a href="sat/index.html">Read more</a></p>
  <p><img width="700" height="375" src="data:image/gif;base64,R0lGODdhAQABAPAAAP///wAAACwAAAAAAQABAEACAkQBADs=" alt="wAAACwAAAAAAQABAEACAkQBADs="><img width="700" height="375" src="wp-content/uploads/2019/10/GRE.jpg" alt="GRE"></p>
  <h4>GRE</h4>
  <p>GRE is the most widely accepted examination by universities to determine a candidate's eligibility for a postgraduate program like MS, MBA, MEM or doctoral degree in USA, Canada and many European Universities.</p>
